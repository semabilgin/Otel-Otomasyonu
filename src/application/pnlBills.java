    private void araBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_araBtnActionPerformed
        DefaultTableModel dtm = new DefaultTableModel();
        dtm.addColumn("Resepsiyonist");
        dtm.addColumn("Müşteri TC");
        dtm.addColumn("Müşteri Adı");
        dtm.addColumn("Müşteri Soyadı");
        dtm.addColumn("Tel");
        dtm.addColumn("Oda numarası");
        dtm.addColumn("Ödenen tutar");
        dtm.addColumn("Tarih");
        try {
            SimpleDateFormat df = new SimpleDateFormat("yyyy-MM-dd hh:mm");
            String gt="",ct="",ara="";
            if (dc1.getDate() != null && dc2.getDate() != null) {
                gt = df.format(dc1.getDate());
                ct = df.format(dc2.getDate());
            }
            if(!txtSearch.getText().trim().equals("")){
                ara = txtSearch.getText().trim()+"*";
            }
//            System.out.println(gt+" "+ct+" "+ara);
            PreparedStatement pr = db.preConnect("call billingSelectPro(?,?,?);");
            pr.setString(1, gt);
            pr.setString(2, ct);
            pr.setString(3, ara);
            ResultSet rs = pr.executeQuery();
            while (rs.next()) {
                bid.add(rs.getString(1));
                dtm.addRow(new String[]{rs.getString(2) + " " + rs.getString(3).toUpperCase(), rs.getString(4).toUpperCase(), rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(8), rs.getString(9), rs.getString(10)});
            }
            billingTable.setModel(dtm);
        } catch (SQLException ex) {
            lg.loggerFunction(ex);
//            System.err.println("fatura arama hatasi " + ex);
        }
    }//GEN-LAST:event_araBtnActionPerformed


