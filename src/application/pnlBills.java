    private void faturalarGetir() {
        DefaultTableModel dtm = new DefaultTableModel();
//        dtm.addColumn("Resepsiyonist");
        dtm.addColumn("Müşteri TC");
        dtm.addColumn("Müşteri Adı");
        dtm.addColumn("Müşteri Soyadı");
        dtm.addColumn("Tel");
        dtm.addColumn("Oda numarası");
        dtm.addColumn("Ödenen tutar");
        dtm.addColumn("Tarih");
        try {
            bid.clear();
            ResultSet rs = db.connect().executeQuery("call billingSelectPro('','','');");
            while (rs.next()) {
                bid.add(rs.getString(1));
                // resepsiyonist bilgisi tableda gosterilmeyecek rs.getString(2) + " " + rs.getString(3).toUpperCase(),
                dtm.addRow(new String[]{ rs.getString(4).toUpperCase(), rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(8), rs.getString(9), rs.getString(10)});
            }
            billingTable.setModel(dtm);

        } catch (SQLException ex) {
            lg.loggerFunction(ex);
//            System.err.println("fatura getirme hatasi " + ex);
        }
    }

